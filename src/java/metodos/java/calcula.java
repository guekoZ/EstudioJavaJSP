package metodos.java;

public class calcula {
    
     private static int resultado;

            public static int suma(int a, int b) {

                resultado = a + b;
                return resultado;
            }

            public static int resta(int a, int b) {

                resultado = a - b;
                return resultado;
            }

             public static int multiplicacion(int a, int b) {

                resultado = a * b;
                return resultado;
            }
    
}
